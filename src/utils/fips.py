"""FIPS code utilities and Texas county reference data."""

from __future__ import annotations

import csv
from pathlib import Path

from src.config import CONFIG_DIR

# Texas state FIPS
TX_STATE_FIPS = "48"


def zero_pad_fips(state_fips: str | int, county_fips: str | int) -> str:
    """Construct a 5-digit zero-padded FIPS code from state and county parts."""
    return f"{int(state_fips):02d}{int(county_fips):03d}"


def parse_fips(fips5: str) -> tuple[str, str]:
    """Split a 5-digit FIPS into (state_fips, county_fips)."""
    fips5 = fips5.zfill(5)
    return fips5[:2], fips5[2:]


def is_texas_fips(fips5: str) -> bool:
    """Check if a 5-digit FIPS belongs to Texas."""
    return fips5.zfill(5).startswith(TX_STATE_FIPS)


def load_fips_crosswalk() -> dict[str, str]:
    """Load the FIPS crosswalk CSV as a dict mapping FIPS -> county name.

    Falls back to an empty dict if the crosswalk file doesn't exist yet.
    """
    path = CONFIG_DIR / "fips_crosswalk.csv"
    if not path.exists():
        return {}
    result = {}
    with open(path, newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            result[row["fips"]] = row["name"]
    return result


